package drinkMachine;

public class AcountBean
{
    private int no;
    private String name;
    private String pas;
    private int money;

    public int getNo()
    {
        return no;
    }

    public String getName()
    {
        return name;
    }

    public String getPas()
    {
        return pas;
    }

    public int getMoney()
    {
        return money;
    }

    public void setNo(int no)
    {
        this.no = no;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setPas(String pas)
    {
        this.pas = pas;
    }

    public void setMoney(int mony)
    {
        this.money = mony;
    }
}
